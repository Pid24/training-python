def caesar_cipher(text, shift, direction):
    result = ""
    if direction == "decode":
        shift *= -1
    for char in text:
        if char.isalpha():
            shift_amount = 65 if char.isupper() else 97
            result += chr((ord(char) - shift_amount + shift) % 26 + shift_amount)
        else:
            result += char
    return result

